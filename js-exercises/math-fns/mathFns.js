// You can use the function from the `Math` module.
function sqrt(value) {
  return Math.sqrt(value);
}
function power(base, powerValue) {
  return Math.pow(base, powerValue);
}
function round(roundValue) {
  return Math.round(roundValue);
}

//Don't change the exported names.
export {
  sqrt,
  power,
  round,
};
